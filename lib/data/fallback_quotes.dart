      return _draw(notSureQuotes, _notSureBag);
    }
  }

  static String _draw(List<String> list, List<int> bag) {
    if (list.isEmpty) return "No quotes found 😭";

    // refill bag when empty
    if (bag.isEmpty) {
      for (int i = 0; i < list.length; i++) {
        bag.add(i);
      }
    }

    // pick random index from bag and remove it
    final pick = _rand(bag.length);
    final chosenIndex = bag.removeAt(pick);
    return list[chosenIndex];
  }
